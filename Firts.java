public class Firts {
    public static void main(String[] args) {
        String n="Purushotham reddy";
        System.out.println(n.toLowerCase());
        System.out.println(n.toUpperCase());
        System.out.println(n.indexOf("a"));
        System.out.println(n.lastIndexOf("u"));
        System.out.println(n.startsWith("Purus"));
        System.out.println(n.endsWith("reddy"));
        System.out.println(n.contains("shoth"));
        System.out.println(n.length());
        System.out.println(n.substring(2, 6));
    }
}
